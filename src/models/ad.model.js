import { Schema, model } from "mongoose";

const adsSchema = new Schema({
  header: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

export default model("Ad", adsSchema);
